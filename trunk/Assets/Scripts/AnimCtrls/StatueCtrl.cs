using UnityEngine;
using System.Collections;

public class StatueCtrl : MonoBehaviour
{
	int waitTime=4;
	Animator animator;
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	void Start()
	{
		animator = GetComponent<Animator>();
		StartCoroutine(DoMove());
	}
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	IEnumerator DoMove()
	{
		yield return new WaitForSeconds(waitTime);
		animator.SetTrigger("Move");
		StartCoroutine(DoMove());
	}
}
